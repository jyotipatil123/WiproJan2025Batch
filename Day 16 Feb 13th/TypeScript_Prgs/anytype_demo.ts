let anything: any;

anything = "Hello, world!";
console.log(anything); // Output: Hello, world!

anything = 42;
console.log(anything); // Output: 42

anything = { key: "value" };
console.log(anything); // Output: { key: 'value' }

anything = [1, 2, 3];
console.log(anything); // Output: [ 1, 2, 3 ]
